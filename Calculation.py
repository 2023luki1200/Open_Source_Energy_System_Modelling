

def add(a, b):
    """add a and b and return result"""
    return a + b


def sub(a, b):
    """subtract b form a and return result"""
    return a - b


def div(a, b):
    """divide a by b and return result"""
    return a / b


def mul(a, b):
    """multiply a with b and return result"""
    return a * b
